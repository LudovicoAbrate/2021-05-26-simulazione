	public List<Business> getVertici(Year anno,String citta){
		
		
		String sql = "select distinct b.* "
				+ "from Business b , reviews r "
				+ "where b.city = ? "
				+ "and r.business_id = b.business_id "
				+ "and YEAR(r.review_date) = ? ";
		
		List<Business> result = new ArrayList<Business>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setString(1,citta);
			st.setInt(2, anno.getValue());
			ResultSet res = st.executeQuery();
			while (res.next()) {

				Business business = new Business(res.getString("business_id"), 
						res.getString("full_address"),
						res.getString("active"),
						res.getString("categories"),
						res.getString("city"),
						res.getInt("review_count"),
						res.getString("business_name"),
						res.getString("neighborhoods"),
						res.getDouble("latitude"),
						res.getDouble("longitude"),
						res.getString("state"),
						res.getDouble("stars"));
				result.add(business);
			}
			res.close();
			st.close();
			conn.close();
			return result;
			
		} catch (SQLException e) {
			e.printStackTrace();
			return null;
		}
	}
	
	public List<Adiacenza> getAdiacenze(Year anno, String citta){
		
		
		String sql = "select distinct r1.business_id bus1 , r2.business_id as bus2 , AVG(r1.stars) as p1, AVG(r2.stars) as p2 "
				+ "from Business b1 , business b2, reviews r1, reviews r2 "
				+ "where b1.city = ? "
				+ "and b1.city = b2.city "
				+ "and r1.business_id = b1.business_id "
				+ "and r2.business_id = b2.business_id "
				+ "and r1.business_id < r2.business_id "
				+ "and b1.business_id < b2.business_id "
				+ "and YEAR(r1.review_date) = ? "
				+ "and YEAR(r1.review_date) = YEAR(r2.review_date) "
				+ "group by bus1,bus2 ";
		
		List<Adiacenza> result = new ArrayList<Adiacenza>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setString(1,citta);
			st.setInt(2, anno.getValue());
			ResultSet res = st.executeQuery();
			while (res.next()) {
				
				//Business b1 = idMap.get(res.getString("bus1"));
				//Business b2 = idMap.get(res.getString("bus2"));
				
				double differenza = res.getDouble("p1") - res.getDouble("p2");
				
				if(differenza > 0 || differenza < 0) {
				
				Adiacenza a = new Adiacenza(res.getString("bus1"),res.getString("bus2"),differenza);
				
				result.add(a);
				}
			}
			res.close();
			st.close();
			conn.close();
			return result;
			
		} catch (SQLException e) {
			e.printStackTrace();
			return null;
		}
	}
	
